/*
 * Get distance
 */

int getDistance() {
	int j, max;
	for (max=1200, j=1200; j<BANK; j++) {		// 110 to ignore first 1m
		if (data[max]<data[j])
			max=j;
	}

	/*
	 * Calculation:
	 * ADC sampling rate 200ksps
	 * Time between samples 5us
	 *    s	= v * t
	 * 		= 340m/s * N/2 * 5us
	 * 		= 170 * 100 * 5u * N cm
	 * 		= 0.085N cm
	 * 	where N is the reading of interest
	 */

	return ((85*max)/1000);
}

/*
 * Moving average
 */

void movingAvg(uint8_t samples) {
	int i,j;
	for (i=0; i<BANK-samples; ++i) {
		for (j=1; j<samples; ++j)
			data[i]+=data[i+j];
		data[i]/=(samples);
	}
}

